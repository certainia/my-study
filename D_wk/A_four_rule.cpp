#include <iostream>
using namespace std;
int main()
{

    int m, n;
    cout << "输入操作数字：" << endl;
    cin >> m >> n;

    cout << m << "+" << n << "=" << m + n << endl;
    cout << m << "-" << n << "=" << m - n << endl;
    cout << m << "*" << n << "=" << m * n << endl;
    cout << m << "/" << n << "=" << m / n << endl;

    return 0;
}
